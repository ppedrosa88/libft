#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char	*p1;
	char	*p2;

	p1 = (char *)s1;
	p2 = (char *)s2;
	while (*p1 != '\0' && *p2 != '\0' && *p1 == *p2 && n--)
	{
		if (n == 0)
			return (0);
		p1++;
		p2++;
	}
	return (*p1 - *p2);
}
